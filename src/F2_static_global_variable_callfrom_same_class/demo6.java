package F2_static_global_variable_callfrom_same_class;

public class demo6
{
	//static global variable call from same class
	
	static float a=45646.4788f;        //static global variable
	static double b=64464.848984;      //static global variable
	static char c='A';                 //static global variable
	static boolean d=true;             //static global variable
	
    public static void main(String[] args)
    {
    	System.out.println(a);         //VariablName;
    	System.out.println(b);         //VariableName;
    	System.out.println(c);         //VariableName;
    	System.out.println(d);	       //VariableName;
    }
	
	

}
