package F2_static_global_variable_callfrom_same_class;

public class demo9
{
	//static global variable call from same class
	
		static float a=121.76f;        //static global variable
		static double b=7890.596590;      //static global variable
		
	
		
	    public static void main(String[] args)
	    {
	    	System.out.println(a);         //VariablName;
	    	System.out.println(b);         //VariableName;
	    	
	    }

}
