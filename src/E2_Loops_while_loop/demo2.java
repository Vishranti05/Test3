package E2_Loops_while_loop;

public class demo2 
{
	//Print even no's from 2 to 10---->2,4,6,8,10
	public static void main(String[] args) 
	{
		int i=2;               //starting condition
		while(i<=10)           //ending condition
		{
			System.out.println(i);
			i=i+2;              //increment
		}
		
	}

}
