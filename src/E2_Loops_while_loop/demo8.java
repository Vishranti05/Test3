package E2_Loops_while_loop;

public class demo8
{
	//Print "Hello" Message 10 times---->Hello,Hello,Hello,Hello
		public static void main(String[] args) 
		{
			int i=1;        //starting condition
			while(i<=10)     //ending condition
			{
				System.out.println("Hello");
				i=i+1;      //increment
			}
			
		}

}
