package E2_Loops_while_loop;

public class demo9 
{
	//Print "Vishranti" Message 15times---
		public static void main(String[] args) 
		{
			int i=1;        //starting condition
			while(i<=15)     //ending condition
			{
				System.out.println("Vishranti");
				i=i+1;      //increment
			}
			
		}

}
