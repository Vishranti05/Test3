package A_variable;

public class Demo_3
{
	public static void main(String[] args)  //main method
	{
		
		//Step1:Variable Declaration
		
		String StudentName;                        //DataType VariableName
		int StudentRollNo;                         //DataType VariableName
		char StudentGrade;                         //DataType VariableName
		float StudentPercentage;                   //DataType VariableName
		
		//Step2:Variable initialization
		
		
		StudentName="Asmita";                   //VariableName="VariableData";
		StudentRollNo=10;                       //VariableName=VariableData;
		StudentGrade='A';                       //VariableName='VariableData';
		StudentPercentage=85.24f;               //VariableName=VariableData*f;
		
		
		//Step3:Variable usage
		
		System.out.println("Student Name:-"+StudentName);             //Asmita
		System.out.println("Student Roll No:-"+StudentRollNo);           //10
		System.out.println("Student Grade:-"+StudentGrade);            // A
		System.out.println("Student Percentage:-"+StudentPercentage+"%");       //85.24
		
		
		
		
	}
	

}
