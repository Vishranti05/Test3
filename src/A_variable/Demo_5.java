package A_variable;

public class Demo_5
{
	public static void main(String[] args)  //main method
	{
		//Step1:Variable declaration
		String StudentName;             //DataType VariableName;
		int StudentRollNo;              //DataType VariableName;
		char StudentGrade;              //DataType VariableName;
		float StudentPercentage;        //DataType VariableName;
		
		//Step2:variable initialization
	   StudentName="Swati";            //variableName="VariableData";
	   StudentRollNo=90;               //variableName=VariableData;
	   StudentGrade='A';               //variableName='VariableData';
	   StudentPercentage=87.32f;      //variableName=VariableData*f;
	   
	   //Step3:vriable Usage
	   System.out.println("Student Name:-"+StudentName);   //Swati
	   System.out.println("Student Roll No:-"+StudentRollNo);  //90
	   System.out.println("Student Grade:-"+StudentGrade);    //A
	   System.out.println("Student Percentage:-"+StudentPercentage+"%"); //87.32
		
		
	}

}
