package A_variable;

public class Demo_1 
{
	public static void main(String[] args)  //Main method
	{
	
	// Step1:Variable declaration
		
		String StudentName;       //DataType variableName;
		int StudentRollNo;        //DataType variableName;
		char StudentGrade;        //DataType variableName;
		float StudentPercentage;  //DataType variableName;
		
    // Step2:Variable initialization
		
		StudentName="Sanvi";       //variable name="variableData";
		StudentRollNo=20;           //variable name=variableData;
		StudentGrade='A';           //variable name='variableData';
		StudentPercentage=70.25f;   //variable name=variableData*f;
				
	//Step3:Variable usage
				
	    System.out.println("Student name:"+StudentName);    //Sandip
	    System.out.println("Student RollNo:"+StudentRollNo);  //20
	    System.out.println("Student Grade:"+StudentGrade);   //A
	    System.out.println("Student percentage:"+StudentPercentage+"%"); //70.25
	}

}
